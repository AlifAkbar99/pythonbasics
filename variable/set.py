print('set of fruits: ')
fruits = {'apple', 'banana', 'orange', 'rambutan'}
print(fruits)
print('')

print('to show how much variables on the set: ')
print(len(fruits))
print('')

print('add wasabi to the set: ')
print(fruits.add('wasabi'))
print(fruits)
print('')

print('remove apple from set: ')
print(fruits.remove('apple'))
print(fruits)
print('')

print('pop the set: ')
print(fruits.pop())
print(fruits)
print('')
